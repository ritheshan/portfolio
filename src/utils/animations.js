// Custom hook for scroll animations
export const useScrollAnimation = (options = {}) => {
  const ref = useRef(null);
  const isInView = useInView(ref, {
    once: true,
    amount: 0.3,
    ...options,
  });

  return {
    ref,
    initial: 'hidden',
    animate: isInView ? 'visible' : 'hidden',
    viewport: { once: true, amount: 0.3 },
  };
};
